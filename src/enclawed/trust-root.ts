// Trust root for module signing.
//
// CRITICAL: the keys below are PLACEHOLDERS. A real classified deployment
// MUST replace them with hardware-anchored keys whose private halves live
// in an HSM controlled by the laboratory ISSO. Shipping with the dev key
// in the trust root makes any signature meaningless. The deploy gate in
// §8.7 of FORK.md exists for this reason.
//
// Each entry binds a base64 SPKI Ed25519 public key to a list of clearance
// levels the signer is approved to attest to. A signer approved for "secret"
// cannot validly sign a manifest that claims "q-cleared", and the verifier
// rejects such a mismatch even if the signature itself is mathematically
// valid.

import type { ClearanceLevel } from "./module-manifest.js";

export type TrustedSigner = Readonly<{
  keyId: string;
  publicKeyPem: string;
  approvedClearance: ReadonlyArray<ClearanceLevel>;
  description: string;
  notAfter?: string;  // ISO date; verifier rejects manifests after this
}>;

