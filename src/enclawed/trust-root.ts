let runtimeTrustRoot: ReadonlyArray<TrustedSigner> = DEFAULT_TRUST_ROOT;
let trustRootLocked = false;

export class TrustRootLockedError extends Error {
  override name = "TrustRootLockedError";
  constructor() {
    super("trust root is locked; setTrustRoot/resetTrustRoot rejected post-lock");
  }
}

export function getTrustRoot(): ReadonlyArray<TrustedSigner> {
  return runtimeTrustRoot;
}

// Replace the trust root at boot time. Call this from a deploying-org
// bootstrap shim that loads trusted signers from a HW-anchored config
// (e.g. files on a verified-boot partition, PKCS#11 reads from an HSM).
// Refuses if the trust root has been locked.
export function setTrustRoot(signers: ReadonlyArray<TrustedSigner>): void {
  if (trustRootLocked) throw new TrustRootLockedError();
  if (!Array.isArray(signers)) {
    throw new TypeError("setTrustRoot: signers must be an array");
  }
  for (const s of signers) {
    if (!s || typeof s.keyId !== "string" || !s.publicKeyPem || !s.approvedClearance) {
      throw new TypeError("setTrustRoot: each signer must define keyId, publicKeyPem, approvedClearance");
    }
  }
  runtimeTrustRoot = Object.freeze(signers.map((s) => Object.freeze({ ...s })));
}

export function findSigner(keyId: string): TrustedSigner | undefined {
  return runtimeTrustRoot.find((s) => s.keyId === keyId);
}

// Lock the trust root so no further mutations succeed. Bootstrap calls this
// in the enclaved flavor immediately after the deploying organization's
// signers are loaded; from that point on, no module code can substitute a
// different trust root inside the running process.
export function lockTrustRoot(): void { trustRootLocked = true; }

export function isTrustRootLocked(): boolean { return trustRootLocked; }
